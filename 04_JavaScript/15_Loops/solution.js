/*
JavaScript Loops Examples
*/

// For loop example
console.log("For Loop:");
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// While loop example
console.log("While Loop:");
let j = 1;
while (j <= 5) {
  console.log(j);
  j++;
}

// Do-While loop example
console.log("Do-While Loop:");
let k = 1;
do {
  console.log(k);
  k++;
} while (k <= 5);
